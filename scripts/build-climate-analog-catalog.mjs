import { spawn } from "child_process";
import { readFile, writeFile } from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const pythonBin = process.env.PYTHON_BIN || "python3";
const catalogYear = 2025;
const scenario = "ssp245";

function runModel(city) {
  return new Promise((resolve, reject) => {
    const child = spawn(
      pythonBin,
      ["grounded_model.py", String(city.lat), String(city.lng), String(catalogYear), scenario],
      { cwd: root },
    );
    let stdout = "";
    let stderr = "";
    child.stdout.on("data", (chunk) => { stdout += chunk; });
    child.stderr.on("data", (chunk) => { stderr += chunk; });
    child.on("error", reject);
    child.on("close", (code) => {
      if (code !== 0) {
        reject(new Error(`grounded_model.py failed for ${city.name}: ${stderr.trim() || `exit ${code}`}`));
        return;
      }
      try {
        resolve(JSON.parse(stdout));
      } catch (err) {
        reject(new Error(`failed to parse grounded_model.py output for ${city.name}: ${err.message}`));
      }
    });
  });
}

function compactProjection(city, projection) {
  return {
    name: city.name,
    country: city.country,
    lat: city.lat,
    lng: city.lng,
    year: catalogYear,
    scenario,
    temperature: {
      annual_mean: projection.temperature.annual_mean,
      monthly: projection.temperature.monthly,
    },
    precipitation: {
      annual_total: projection.precipitation.annual_total,
      monthly: projection.precipitation.monthly,
    },
    extremes: {
      heat_stress_days: projection.extremes.heat_stress_days,
      drought_risk: projection.extremes.drought_risk,
      flood_risk: projection.extremes.flood_risk,
    },
    metadata: {
      baseline_source: projection.metadata?.baseline_source,
    },
  };
}

const cities = JSON.parse(await readFile(path.join(root, "data", "ranking_cities.json"), "utf8"));
const candidates = [];

for (const city of cities) {
  const projection = await runModel(city);
  candidates.push(compactProjection(city, projection));
}

const payload = {
  version: "grounded-current-analogs-v1",
  catalogYear,
  scenario,
  candidateCount: candidates.length,
  method:
    "Present-day analog candidates are generated by grounded_model.py at 2025 for the fixed ranking_cities catalog. The client computes nearest-neighbor climate distance over standardized monthly temperature and log monthly precipitation vectors.",
  source:
    "WorldClim v2.1 observed baseline where available + CMIP6 ScenarioMIP + IPCC AR6 calibrated temperature, via grounded_model.py",
  candidates,
};

await writeFile(
  path.join(root, "client/public/climate-analog-catalog.2025.json"),
  `${JSON.stringify(payload, null, 2)}\n`,
);

console.log(`wrote ${candidates.length} analog candidates for ${catalogYear}`);
