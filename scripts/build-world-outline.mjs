// One-off generator: fetch Natural Earth 110m land, project to the twin-arc's
// equirectangular viewBox (660x300), and emit a compact SVG path module so the
// climate-twin map shows real continents (not an empty grid).
// Run: node scripts/build-world-outline.mjs
import { writeFileSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const url = "https://raw.githubusercontent.com/nvkelso/natural-earth-vector/v5.1.2/geojson/ne_110m_land.geojson";
const W = 660, H = 300;
const px = (lng) => Number(((lng + 180) / 360 * W).toFixed(1));
const py = (lat) => Number(((90 - lat) / 180 * H).toFixed(1));

const res = await fetch(url, { headers: { "user-agent": "fupit" } });
if (!res.ok) throw new Error(`NE land fetch failed: HTTP ${res.status}`);
const geo = await res.json();

let d = "";
for (const f of geo.features) {
  const polys = f.geometry.type === "Polygon" ? [f.geometry.coordinates] : f.geometry.coordinates;
  for (const poly of polys) {
    const ring = poly[0];
    if (!ring || ring.length < 8) continue;
    let seg = "";
    for (let i = 0; i < ring.length; i += 2) {
      const c = ring[i];
      seg += `${i === 0 ? "M" : "L"}${px(c[0])} ${py(c[1])} `;
    }
    d += `${seg}Z `;
  }
}
d = d.trim();

const out = `// Auto-generated by scripts/build-world-outline.mjs — Natural Earth 110m land,
// projected to the twin-arc equirectangular viewBox 0 0 660 300. Do not edit by hand.
export const WORLD_OUTLINE_PATH = ${JSON.stringify(d)};
export const WORLD_OUTLINE_VIEWBOX = { w: 660, h: 300 };
`;
writeFileSync(path.join(root, "client/src/lib/world-outline.ts"), out);
console.log(`wrote world-outline.ts (${d.length} path chars)`);
