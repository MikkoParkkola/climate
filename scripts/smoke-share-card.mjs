import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const appPath = path.join(repoRoot, "client", "src", "pages", "climate-app.tsx");
const source = fs.readFileSync(appPath, "utf8");
const helpers = fs.readFileSync(path.join(repoRoot, "client", "src", "lib", "climate-helpers.ts"), "utf8");
const charts = fs.readFileSync(path.join(repoRoot, "client", "src", "components", "climate-charts.tsx"), "utf8");
const shareCard = fs.readFileSync(path.join(repoRoot, "client", "src", "lib", "share-card.ts"), "utf8");
const constants = fs.readFileSync(path.join(repoRoot, "client", "src", "lib", "climate-constants.ts"), "utf8");
const types = fs.readFileSync(path.join(repoRoot, "client", "src", "lib", "climate-types.ts"), "utf8");
const derivations = fs.readFileSync(path.join(repoRoot, "client", "src", "lib", "climate-derivations.ts"), "utf8");
const landing = fs.readFileSync(path.join(repoRoot, "client", "src", "components", "climate-landing.tsx"), "utf8");
const all = [source, helpers, charts, shareCard, constants, types, derivations, landing].join("\n");

assert.match(all, /const shareStory = useMemo/, "share card derives a versioned story from live result data");
assert.match(all, /scoreStory\.scoreDrivers\[0\]/, "share card includes the top visible score driver");
assert.match(all, /climateAnalog\.candidate\.name/, "share card can use the bounded climate-twin match");
assert.match(all, /Shareable climate story/, "result page renders the shareable story card");
assert.match(all, /Share story/, "share card exposes a one-click share control");
assert.match(all, /Copy story/, "share card exposes a clipboard fallback");
assert.match(all, /navigator\.share/, "share flow uses the platform share API when available");
assert.match(all, /function buildShareImageSvg/, "share card can generate a social image from the same grounded story data");
assert.match(all, /function svgToPngBlob/, "share image renderer converts the SVG card to a PNG download");
assert.match(all, /image\/png/, "share image renderer emits a PNG social image");
assert.match(all, /Download image/, "share card exposes the generated image control");
assert.match(all, /TOP LOCAL DRIVER/, "social image includes the key local trend driver");
assert.match(all, /CLIMATE TWIN/, "social image includes the climate-twin analog");
assert.match(all, /const learningPrompts = useMemo<LearningPrompt\[\]>/, "result page derives grounded comparison prompts from the visible forecast");
assert.match(all, /Questions to test next/, "result page renders comparison prompts for follow-up learning");
assert.match(all, /Pathway question/, "learning prompts include a scenario-pathway comparison");
assert.match(all, /Twin question/, "learning prompts include a climate-twin comparison");
assert.match(all, /Side-by-side question/, "learning prompts include a location comparison");
assert.match(all, /void loadScenarioContrast\(\)/, "pathway prompt reuses the existing scenario contrast flow");
assert.match(all, /window\.location\.href = "\/comparison"/, "side-by-side prompt reuses the comparison view");
assert.match(all, /not advice to move, invest, insure, or rank safe havens/, "learning prompts include a non-advisory scope caveat");
assert.match(all, /bounded climate-twin catalog/, "share card discloses bounded analog catalog coverage");
assert.match(all, /no unregistered enrichments/, "share card forbids unregistered enrichment claims");
assert.match(all, /no safe-city claim/, "share card forbids safe-city claims");
assert.doesNotMatch(source, /recruiter|business opportunit|traffic source|showcase funnel/i, "public share card must not leak private visibility strategy");

console.log("share-card smoke passed: one-click story, social image, climate twin, trend driver, and caveats guarded");
